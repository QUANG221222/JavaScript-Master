//While: Dùng khi muốn lặp lại 1 đoạn code cho đến khi điều kiện không còn đúng nữa
let i = 0
//let i = 11 //Dùng để so sánh với do ... while
while (i < 11) {
  console.log(`Count : ${i}`)
  i++
}
