//Comparison Operators: Toán tử so sánh

let x = 10
let y = 5
// let y = '10'

console.log(x == y) // bằng nhau
console.log(x === y) // bằng nhau và phải cùng kiểu dữ liệu
console.log(x != y) //Không bằng nhau
console.log(x !== y) //Không bằng nhau và phải cùng kiểu dữ liệu
console.log(x > y) //x lớn hơn y
console.log(x < y) //x nhỏ hơn y
console.log(x >= y) //x lớn hơn hoặc bằng y
console.log(x <= y) //x nhỏ hơn hoặc bằng y
