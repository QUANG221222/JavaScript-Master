//Assignment Operators: Toán tử gán
let x = 10 // gán giá trị 10 cho biến x
console.log(x) // 10

x += 5 //tương đương với x = x + 5
console.log(x) // 15

x -= 3 //tương đương với x = x - 3
console.log(x) // 12

x *= 2 //tương đương với x = x * 2
console.log(x) // 24

x /= 4 //tương đương với x = x / 4
console.log(x) // 6

x %= 3 //tương đương với x = x % 3
console.log(x) // 0
